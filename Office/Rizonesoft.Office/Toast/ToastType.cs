namespace Rizonesoft.Office.Toast;

/// <summary>
/// Enumerates various types of toast notifications that will revolutionize communication, one toast at a time.
/// </summary>
public enum ToastType
{
    /// <summary>
    /// Represents a message toast, delivering essential information with a sense of style and panache.
    /// </summary>
    MessageToast,

    /// <summary>
    /// Represents a notification toast, conveying vital updates with a touch of class and sophistication.
    /// </summary>
    NotificationToast,

    /// <summary>
    /// Represents a language toast, showcasing linguistic versatility and breaking barriers.
    /// </summary>
    LanguageToast,

    /// <summary>
    /// Represents an update toast, announcing the latest and greatest features with pride and anticipation.
    /// </summary>
    UpdateToast
}